// Issues and discussions: in-repo, ref-tracked, signable.
//
// Storage model
// =============
// Each issue or discussion lives at a single ref: `refs/issues/<N>`.
// The ref points to a blob whose payload is the TOML-encoded current
// state (header + ordered events). Updates are write-the-whole-blob,
// serialized through `repo.lock()`.
//
// Why ref-to-blob and not commit-history-per-issue:
//
//   - The wire protocol already pushes/pulls every ref under refs/, so
//     ref-to-blob travels with `clone`, `fetch`, and `push` with no
//     protocol changes.
//   - The reflog already records every ref movement, so a per-issue
//     history is available via `gyt reflog refs/issues/<N>` without us
//     needing a parallel event store.
//   - Loose objects are content-addressed; identical states deduplicate
//     for free across forks.
//   - One ref → one blob keeps reads O(1) and writes O(state size). For
//     large discussions we revisit this, but XZ on the wire and on disk
//     makes the "rewrite the whole TOML on every comment" model cheap.
//
// Format (TOML)
// =============
// The blob payload is canonical TOML — we hand-serialize so that two
// runs that produce the same logical state produce byte-identical
// blobs (important for dedup and for clean diffs in `gyt reflog`):
//
//     # generated by gyt; canonical ordering matters
//     schema_version = 1
//     kind = "issue"          # or "discussion"
//     number = 42
//     title = "..."
//     state = "open"          # or "closed"
//     author = "Name <email>"
//     created_ts = 1715000000
//     labels = ["bug", "p1"]
//     assignees = ["Name <email>", ...]
//     mentions = [12, 13]     # other issue numbers referenced in any body
//
//     [[event]]
//     kind = "open"
//     author = "Name <email>"
//     ts = 1715000000
//     body = "..."
//
//     [[event]]
//     kind = "comment"
//     author = "Name <email>"
//     ts = 1715000100
//     body = "..."
//
//     [[event]]
//     kind = "label"
//     author = "Name <email>"
//     ts = 1715000200
//     add = ["bug"]
//     remove = []
//
//     [[event]]
//     kind = "assign"
//     author = "Name <email>"
//     ts = 1715000300
//     add = ["Name <email>"]
//     remove = []
//
//     [[event]]
//     kind = "close"
//     author = "Name <email>"
//     ts = 1715001000
//     reason = "fixed in #44"
//
//     [[event]]
//     kind = "reopen"
//     author = "Name <email>"
//     ts = 1715002000
//
// The header fields (title, state, labels, …) are kept in sync with
// the event log so a reader doesn't have to fold events to render the
// current state. The fold is recomputed on every write.

