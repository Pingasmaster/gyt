/// Atomically allocate the next issue number. Caller must already hold
/// `repo.lock()`; we don't take it ourselves because most callers want
/// to allocate + write the issue under one critical section.
pub fn next_number_locked(repo: &Repo) -> Result<u64> {
    let p = repo.gyt_dir.join(COUNTER_PATH);
    if let Some(parent) = p.parent() {
        std::fs::create_dir_all(parent)?;
    }
    let next: u64 = if p.exists() {
        let body = fs_util::read_all(&p)?;
        let txt = std::str::from_utf8(&body)
            .map_err(|_| GytError::Refs("issues_next is not utf-8".into()))?;
        txt.trim()
            .parse::<u64>()
            .map_err(|_| GytError::Refs("issues_next is not a number".into()))?
    } else {
        1
    };
    // L16: checked_add so a counter at u64::MAX doesn't panic in debug
    // (test runner) or wrap silently in release.
    let bumped = next.checked_add(1).ok_or_else(|| {
        GytError::Refs("issues_next counter overflow (u64::MAX)".into())
    })?;
    fs_util::atomic_write(&p, format!("{bumped}\n").as_bytes())?;
    Ok(next)
}

/// Validate that `new` is a legitimate monotonic-append update to
/// `old`. Closes C3/F-D8-02: previously the wire path accepted any
/// `refs/issues/<N>` blob, so an rw client could push a blob with
/// `events = []` and silently erase the entire issue history. Now we
/// require:
///
/// - same `number`, `kind`, `created_ts`, `author`, `title` (header
///   fields can never change after creation), and
/// - `new.events` is a prefix-extension of `old.events` (existing
///   events are byte-for-byte preserved, only new events appended).
///
/// `state` and `labels`/`assignees`/`mentions` MAY change between
/// versions (closing an issue / adding a label is a legitimate
/// update); those don't need a matching event today.
pub fn validate_extends(old: &Issue, new: &Issue) -> Result<()> {
    if new.number != old.number {
        return Err(GytError::Refs(format!(
            "issue rewind: number changed {} -> {}",
            old.number, new.number
        )));
    }
    if new.kind != old.kind {
        return Err(GytError::Refs("issue rewind: kind changed".into()));
    }
    if new.created_ts != old.created_ts {
        return Err(GytError::Refs("issue rewind: created_ts changed".into()));
    }
    if new.author != old.author {
        return Err(GytError::Refs("issue rewind: author changed".into()));
    }
    if new.title != old.title {
        return Err(GytError::Refs("issue rewind: title changed".into()));
    }
    if new.events.len() < old.events.len() {
        return Err(GytError::Refs(format!(
            "issue rewind: event count decreased {} -> {}",
            old.events.len(),
            new.events.len()
        )));
    }
    for (i, oe) in old.events.iter().enumerate() {
        // Length already validated above so .get is Some.
        if new.events.get(i) != Some(oe) {
            return Err(GytError::Refs(format!(
                "issue rewind: event #{i} was modified"
            )));
        }
    }
    // B4: timestamps must be non-decreasing across the chain. Without
    // this, a client can push events with `ts = 0` or with ts going
    // backwards, and the renderer (which orders by file position, not
    // timestamp) shows them in the wrong order. The check is non-strict
    // — same-second events are legitimate (a script can `gyt issue
    // comment` twice within the same wall-clock second).
    for w in new.events.windows(2) {
        if let [a, b] = w
            && b.ts < a.ts
        {
            return Err(GytError::Refs(format!(
                "issue event ts not monotonic: {} -> {}",
                a.ts, b.ts
            )));
        }
    }
    Ok(())
}

pub fn ref_name(n: u64) -> String {
    format!("{ISSUE_REFS_PREFIX}/{n}")
}

/// Read the issue stored at refs/issues/<n>. Returns NotFound if the
/// ref doesn't exist, regardless of whether the underlying blob is
/// present in the object store (a missing blob is a hard error worth
/// surfacing).
pub fn read(repo: &Repo, n: u64) -> Result<Issue> {
    let id = match refs::read_ref(&repo.gyt_dir, &ref_name(n)) {
        Ok(id) => id,
        Err(GytError::Refs(_)) => return Err(GytError::NotFound(format!("issue #{n}"))),
        Err(e) => return Err(e),
    };
    let iss = read_blob(&repo.gyt_dir, &id)?;
    // L17: cross-check the on-disk blob's `number` field against the
    // ref's N. A malicious / corrupted push could otherwise point
    // refs/issues/5 at a blob claiming `number = 99`, and `gyt issue
    // show 5` would print "#99".
    if iss.number != n {
        return Err(GytError::Refs(format!(
            "issue blob at refs/issues/{n} claims number={}", iss.number
        )));
    }
    Ok(iss)
}

fn read_blob(repo_gyt: &Path, id: &ObjectId) -> Result<Issue> {
    let obj = object::store::read(repo_gyt, id)?;
    if obj.kind != ObjectKind::Blob {
        return Err(GytError::Refs(format!(
            "refs/issues blob expected, found {}",
            obj.kind.as_str()
        )));
    }
    decode(&obj.payload)
}

/// Write an issue's full state. Returns the new blob id. Caller must
/// hold `repo.lock()` to prevent concurrent writers from interleaving
/// updates.
pub fn write_locked(repo: &Repo, issue: &Issue) -> Result<ObjectId> {
    let bytes = encode(issue);
    let id = object::store::write_bytes(&repo.gyt_dir, ObjectKind::Blob, &bytes)?;
    refs::write_ref(&repo.gyt_dir, &ref_name(issue.number), &id)?;
    Ok(id)
}

/// List all issues (open + closed) in ascending number order.
pub fn list(repo: &Repo) -> Result<Vec<Issue>> {
    let refs_ = refs::list_refs(&repo.gyt_dir, ISSUE_REFS_PREFIX)?;
    let mut out = Vec::with_capacity(refs_.len());
    for (name, id) in refs_ {
        // The ref-walker may yield names like `refs/issues/12`; skip
        // anything that doesn't parse as a number, so a future
        // sub-namespace (`refs/issues/meta/...`) can't crash listing.
        let Some(_n_str) = name.strip_prefix(&format!("{ISSUE_REFS_PREFIX}/")) else {
            continue;
        };
        // A corrupt single issue should not break `list`. Skip and let
        // `gyt issue show <N>` surface the real error.
        if let Ok(i) = read_blob(&repo.gyt_dir, &id) {
            out.push(i);
        }
    }
    out.sort_by_key(|i| i.number);
    Ok(out)
}

// ─── Helpers used by the CLI ──────────────────────────────────────────

/// Extract `#N` mentions from arbitrary text. Returns a sorted, deduped
/// list of issue numbers. A mention is a `#` followed by 1-10 digits,
/// not preceded by an alphanumeric character (so `abc#1` doesn't match
/// — that's almost certainly part of a longer token).
#[must_use]
#[expect(
    clippy::indexing_slicing,
    clippy::string_slice,
    reason = "args[i] / similar indexing is gated by an explicit bounds check on a preceding line; byte offsets used are at ASCII / char-boundary positions by construction"
)]
pub fn extract_mentions(text: &str) -> Vec<u64> {
    let bytes = text.as_bytes();
    let mut out: Vec<u64> = Vec::new();
    let mut i = 0;
    while i < bytes.len() {
        if bytes[i] == b'#'
            && (i == 0 || !is_word_char(bytes[i - 1]))
            && i + 1 < bytes.len()
            && bytes[i + 1].is_ascii_digit()
        {
            let mut j = i + 1;
            while j < bytes.len() && j - i <= 11 && bytes[j].is_ascii_digit() {
                j += 1;
            }
            // Require a non-word terminator (or EOF) to rule out `#1abc`.
            if (j == bytes.len() || !is_word_char(bytes[j]))
                && let Ok(n) = text[i + 1..j].parse::<u64>()
            {
                out.push(n);
            }
            i = j;
        } else {
            i += 1;
        }
    }
    out.sort_unstable();
    out.dedup();
    out
}

const fn is_word_char(b: u8) -> bool {
    b.is_ascii_alphanumeric() || b == b'_'
}

/// Merge new mentions into the issue's mentions list, keeping order
/// sorted and deduplicated.
pub fn merge_mentions(into: &mut Vec<u64>, new: &[u64], self_number: u64) {
    for n in new {
        if *n == self_number {
            continue;
        }
        if !into.contains(n) {
            into.push(*n);
        }
    }
    into.sort_unstable();
    into.dedup();
}

