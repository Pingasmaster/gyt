pub const SCHEMA_VERSION: u32 = 1;
pub const ISSUE_REFS_PREFIX: &str = "refs/issues";
const COUNTER_PATH: &str = "meta/issues_next";

/// The whole on-disk representation of one issue or discussion.
#[derive(Debug, Clone)]
pub struct Issue {
    pub number: u64,
    pub kind: IssueKind,
    pub title: String,
    pub state: IssueState,
    pub author: String,
    pub created_ts: i64,
    pub labels: Vec<String>,
    pub assignees: Vec<String>,
    pub mentions: Vec<u64>,
    pub events: Vec<Event>,
}

#[derive(Debug, Clone, Copy, PartialEq, Eq)]
pub enum IssueKind {
    Issue,
    Discussion,
}

impl IssueKind {
    pub const fn as_str(self) -> &'static str {
        match self {
            Self::Issue => "issue",
            Self::Discussion => "discussion",
        }
    }
    pub fn parse(s: &str) -> Result<Self> {
        match s {
            "issue" => Ok(Self::Issue),
            "discussion" => Ok(Self::Discussion),
            other => Err(GytError::Refs(format!("unknown issue kind: {other}"))),
        }
    }
}

#[derive(Debug, Clone, Copy, PartialEq, Eq)]
pub enum IssueState {
    Open,
    Closed,
}

impl IssueState {
    pub const fn as_str(self) -> &'static str {
        match self {
            Self::Open => "open",
            Self::Closed => "closed",
        }
    }
    pub fn parse(s: &str) -> Result<Self> {
        match s {
            "open" => Ok(Self::Open),
            "closed" => Ok(Self::Closed),
            other => Err(GytError::Refs(format!("unknown issue state: {other}"))),
        }
    }
}

#[derive(Debug, Clone, PartialEq, Eq)]
pub struct Event {
    pub kind: EventKind,
    pub author: String,
    pub ts: i64,
    pub body: String,
    pub add: Vec<String>,
    pub remove: Vec<String>,
    pub reason: String,
}

#[derive(Debug, Clone, Copy, PartialEq, Eq)]
pub enum EventKind {
    Open,
    Comment,
    Close,
    Reopen,
    Label,
    Assign,
}

impl EventKind {
    pub const fn as_str(self) -> &'static str {
        match self {
            Self::Open => "open",
            Self::Comment => "comment",
            Self::Close => "close",
            Self::Reopen => "reopen",
            Self::Label => "label",
            Self::Assign => "assign",
        }
    }
    pub fn parse(s: &str) -> Result<Self> {
        match s {
            "open" => Ok(Self::Open),
            "comment" => Ok(Self::Comment),
            "close" => Ok(Self::Close),
            "reopen" => Ok(Self::Reopen),
            "label" => Ok(Self::Label),
            "assign" => Ok(Self::Assign),
            other => Err(GytError::Refs(format!("unknown event kind: {other}"))),
        }
    }
}

// ─── Encoding / decoding ──────────────────────────────────────────────

/// Encode an issue to canonical TOML bytes. Field ordering is fixed so
/// equivalent issues produce identical blobs.
#[expect(
    clippy::unwrap_used,
    reason = "writeln! to String never fails; the Result is only present for io::Write compatibility"
)]
pub fn encode(issue: &Issue) -> Vec<u8> {
    let mut s = String::new();
    s.push_str("# generated by gyt; canonical ordering matters\n");
    writeln!(s, "schema_version = {SCHEMA_VERSION}").unwrap();
    writeln!(s, "kind = {:?}", issue.kind.as_str()).unwrap();
    writeln!(s, "number = {}", issue.number).unwrap();
    writeln!(s, "title = {:?}", issue.title).unwrap();
    writeln!(s, "state = {:?}", issue.state.as_str()).unwrap();
    writeln!(s, "author = {:?}", issue.author).unwrap();
    writeln!(s, "created_ts = {}", issue.created_ts).unwrap();
    writeln!(s, "labels = {}", toml_string_array(&issue.labels)).unwrap();
    writeln!(s, "assignees = {}", toml_string_array(&issue.assignees)).unwrap();
    writeln!(s, "mentions = {}", toml_u64_array(&issue.mentions)).unwrap();
    s.push('\n');
    for e in &issue.events {
        s.push_str("[[event]]\n");
        writeln!(s, "kind = {:?}", e.kind.as_str()).unwrap();
        writeln!(s, "author = {:?}", e.author).unwrap();
        writeln!(s, "ts = {}", e.ts).unwrap();
        writeln!(s, "body = {:?}", e.body).unwrap();
        writeln!(s, "add = {}", toml_string_array(&e.add)).unwrap();
        writeln!(s, "remove = {}", toml_string_array(&e.remove)).unwrap();
        writeln!(s, "reason = {:?}", e.reason).unwrap();
        s.push('\n');
    }
    s.into_bytes()
}

/// Decode an issue from a TOML blob. The parser is hand-rolled — we
/// don't add a TOML dependency. The format is restricted enough that a
/// straight line scan handles every encode() output, and any malformed
/// input is rejected with a clean error rather than panicking.
pub fn decode(bytes: &[u8]) -> Result<Issue> {
    let text = std::str::from_utf8(bytes)
        .map_err(|_| GytError::Refs("issue blob is not valid utf-8".into()))?;

    let mut number: Option<u64> = None;
    let mut kind: Option<IssueKind> = None;
    let mut title = String::new();
    let mut state: Option<IssueState> = None;
    let mut author = String::new();
    let mut created_ts: i64 = 0;
    let mut labels: Vec<String> = Vec::new();
    let mut assignees: Vec<String> = Vec::new();
    let mut mentions: Vec<u64> = Vec::new();
    let mut schema_seen = false;

    let mut events: Vec<Event> = Vec::new();
    let mut cur: Option<Event> = None;

    for raw in text.lines() {
        let line = raw.trim();
        if line.is_empty() || line.starts_with('#') {
            continue;
        }
        if line == "[[event]]" {
            if let Some(e) = cur.take() {
                events.push(e);
            }
            cur = Some(Event {
                kind: EventKind::Comment,
                author: String::new(),
                ts: 0,
                body: String::new(),
                add: Vec::new(),
                remove: Vec::new(),
                reason: String::new(),
            });
            continue;
        }
        let (key, val) = line
            .split_once('=')
            .ok_or_else(|| GytError::Refs(format!("issue blob: malformed line: {raw}")))?;
        let key = key.trim();
        let val = val.trim();
        if let Some(e) = cur.as_mut() {
            match key {
                "kind" => e.kind = EventKind::parse(&parse_toml_string(val)?)?,
                "author" => e.author = parse_toml_string(val)?,
                "ts" => e.ts = val.parse::<i64>().map_err(|_| invalid("event.ts"))?,
                "body" => e.body = parse_toml_string(val)?,
                "add" => e.add = parse_toml_string_array(val)?,
                "remove" => e.remove = parse_toml_string_array(val)?,
                "reason" => e.reason = parse_toml_string(val)?,
                other => return Err(invalid(&format!("unknown event field: {other}"))),
            }
        } else {
            match key {
                "schema_version" => {
                    let v: u32 = val.parse().map_err(|_| invalid("schema_version"))?;
                    if v != SCHEMA_VERSION {
                        return Err(GytError::Refs(format!(
                            "unsupported issue schema_version {v}"
                        )));
                    }
                    schema_seen = true;
                }
                "kind" => kind = Some(IssueKind::parse(&parse_toml_string(val)?)?),
                "number" => number = Some(val.parse().map_err(|_| invalid("number"))?),
                "title" => title = parse_toml_string(val)?,
                "state" => state = Some(IssueState::parse(&parse_toml_string(val)?)?),
                "author" => author = parse_toml_string(val)?,
                "created_ts" => {
                    created_ts = val.parse::<i64>().map_err(|_| invalid("created_ts"))?;
                }
                "labels" => labels = parse_toml_string_array(val)?,
                "assignees" => assignees = parse_toml_string_array(val)?,
                "mentions" => mentions = parse_toml_u64_array(val)?,
                other => return Err(invalid(&format!("unknown issue field: {other}"))),
            }
        }
    }
    if let Some(e) = cur.take() {
        events.push(e);
    }
    if !schema_seen {
        return Err(invalid("missing schema_version"));
    }
    let number = number.ok_or_else(|| invalid("missing number"))?;
    let kind = kind.ok_or_else(|| invalid("missing kind"))?;
    let state = state.ok_or_else(|| invalid("missing state"))?;
    Ok(Issue {
        number,
        kind,
        title,
        state,
        author,
        created_ts,
        labels,
        assignees,
        mentions,
        events,
    })
}

fn invalid(field: &str) -> GytError {
    GytError::Refs(format!("issue blob: invalid {field}"))
}

fn toml_string_array(xs: &[String]) -> String {
    if xs.is_empty() {
        return "[]".into();
    }
    let parts: Vec<String> = xs.iter().map(|s| format!("{s:?}")).collect();
    format!("[{}]", parts.join(", "))
}

fn toml_u64_array(xs: &[u64]) -> String {
    if xs.is_empty() {
        return "[]".into();
    }
    let parts: Vec<String> = xs.iter().map(u64::to_string).collect();
    format!("[{}]", parts.join(", "))
}
#[expect(
    clippy::string_slice,
    reason = "byte offsets used are at ASCII / char-boundary positions by construction"
)]
fn parse_toml_string(s: &str) -> Result<String> {
    // Accept ONLY a literal `"..."` form using Rust's debug formatting
    // (\\, \", \n, \r, \t). No multi-line strings, no raw strings, no
    // bare keys — the encoder never emits those.
    let s = s.trim();
    if s.len() < 2 || !s.starts_with('"') || !s.ends_with('"') {
        return Err(GytError::Refs(format!("issue blob: not a quoted string: {s}")));
    }
    let inner = &s[1..s.len() - 1];
    let mut out = String::with_capacity(inner.len());
    let mut iter = inner.chars();
    while let Some(c) = iter.next() {
        if c == '\\' {
            match iter.next() {
                Some('n') => out.push('\n'),
                Some('r') => out.push('\r'),
                Some('t') => out.push('\t'),
                Some('\\') => out.push('\\'),
                Some('"') => out.push('"'),
                Some('0') => out.push('\0'),
                Some(other) => {
                    return Err(GytError::Refs(format!(
                        "issue blob: unsupported escape \\{other}"
                    )));
                }
                None => return Err(GytError::Refs("issue blob: trailing backslash".into())),
            }
        } else {
            out.push(c);
        }
    }
    Ok(out)
}
#[expect(
    clippy::string_slice,
    reason = "byte offsets used are at ASCII / char-boundary positions by construction"
)]
fn parse_toml_string_array(s: &str) -> Result<Vec<String>> {
    let s = s.trim();
    if s == "[]" {
        return Ok(Vec::new());
    }
    if !s.starts_with('[') || !s.ends_with(']') {
        return Err(GytError::Refs(format!("issue blob: not an array: {s}")));
    }
    let inner = &s[1..s.len() - 1];
    let mut out = Vec::new();
    let mut buf = String::new();
    let mut in_str = false;
    let mut escape = false;
    for c in inner.chars() {
        if escape {
            buf.push(c);
            escape = false;
            continue;
        }
        if in_str {
            buf.push(c);
            if c == '\\' {
                escape = true;
            } else if c == '"' {
                in_str = false;
            }
            continue;
        }
        if c == '"' {
            buf.push(c);
            in_str = true;
        } else if c == ',' {
            out.push(parse_toml_string(buf.trim())?);
            buf.clear();
        } else if !c.is_whitespace() {
            buf.push(c);
        }
    }
    if !buf.trim().is_empty() {
        out.push(parse_toml_string(buf.trim())?);
    }
    Ok(out)
}
#[expect(
    clippy::string_slice,
    reason = "byte offsets used are at ASCII / char-boundary positions by construction"
)]
fn parse_toml_u64_array(s: &str) -> Result<Vec<u64>> {
    let s = s.trim();
    if s == "[]" {
        return Ok(Vec::new());
    }
    if !s.starts_with('[') || !s.ends_with(']') {
        return Err(GytError::Refs(format!("issue blob: not an array: {s}")));
    }
    let inner = &s[1..s.len() - 1];
    let mut out = Vec::new();
    for tok in inner.split(',') {
        let t = tok.trim();
        if t.is_empty() {
            continue;
        }
        out.push(t.parse::<u64>().map_err(|_| invalid("u64 in array"))?);
    }
    Ok(out)
}

