    #[test]
    fn round_trip_preserves_every_field() {
        let issue = fixture();
        let bytes = encode(&issue);
        let back = decode(&bytes).unwrap();
        assert_eq!(back.number, issue.number);
        assert_eq!(back.title, issue.title);
        assert_eq!(back.state, issue.state);
        assert_eq!(back.kind, issue.kind);
        assert_eq!(back.author, issue.author);
        assert_eq!(back.created_ts, issue.created_ts);
        assert_eq!(back.labels, issue.labels);
        assert_eq!(back.assignees, issue.assignees);
        assert_eq!(back.mentions, issue.mentions);
        assert_eq!(back.events.len(), issue.events.len());
        for (a, b) in back.events.iter().zip(issue.events.iter()) {
            assert_eq!(a.kind, b.kind);
            assert_eq!(a.author, b.author);
            assert_eq!(a.ts, b.ts);
            assert_eq!(a.body, b.body);
            assert_eq!(a.add, b.add);
            assert_eq!(a.remove, b.remove);
            assert_eq!(a.reason, b.reason);
        }
    }

